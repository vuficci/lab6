# Создание переменной для имени
name = "Виктория"

# Преобразование имени в верхний регистр и вывод результата
uppercase_name = name.upper()
print("Имя в верхнем регистре:", uppercase_name)

# Преобразование имени в нижний регистр и вывод результата
lowercase_name = name.lower()
print("Имя в нижнем регистре:", lowercase_name)